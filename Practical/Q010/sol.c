// switch with examples

#include <stdio.h>

int main(){
    int  caseNum = 3;

    switch (caseNum){
        case 1:
            printf("Choice 1 is selected");
            break;
        
        case 2 :
            printf("Choice 2 is selected");
            break;
        
        default:
            printf("No choice found");
            break;
        

    }
    return 0;
}