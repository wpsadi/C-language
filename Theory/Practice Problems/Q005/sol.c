// program to check if a number is divisible by 2

#include <stdio.h>

int main()
{
    printf("Enter the Number you want to check : ");

    int num;
    scanf("%d", &num);

    printf("\n");

    if (num % 2 == 0)
    {
        printf("%d is not divisible by 2", num);
    }
    else
    {
        printf("%d is not divisible by 2", num);
    }

    return 0;
}